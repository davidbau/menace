/**
 * Crypt Level (burial chambers)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel');

    // Burial crypt with tombs
    des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|\
|..----------.----------.----------.----------.----------.----------.....|\
|..|........|.|........|.|........|.|........|.|........|.|........|.....|\
|..|........|.|........|.|........|.|........|.|........|.|........|.....|\
|..|........|.|........|.|........|.|........|.|........|.|........|.....|\
|..----+-----.----+-----.----+-----.----+-----.----+-----.----+-----......|\
|.........................................................................|\
|.........................................................................|\
|..----------.----------.----------.----------.----------.----------.....|\
|..|........|.|........|.|........|.|........|.|........|.|........|.....|\
|..|........|.|........|.|........|.|........|.|........|.|........|.....|\
|..|........|.|........|.|........|.|........|.|........|.|........|.....|\
|..----+-----.----+-----.----+-----.----+-----.----+-----.----+-----......|\
|.........................................................................|\
|.........................................................................|\
|.........................................................................|\
---------------------------------------------------------------------------
`
    });

    // Dimly lit
    des.region(selection.area(1, 1, 73, 15), 'unlit');

    // Stairs
    des.stair('up', 5, 7);
    des.stair('down', 68, 14);

    // Tomb doors
    for (let y of [6, 13]) {
        for (let x of [6, 16, 26, 36, 46, 56]) {
            des.door('closed', x, y);
        }
    }

    // Non-diggable
    des.non_diggable();

    // Burial goods
    for (let i = 0; i < 15; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 12; i++) {
        des.trap();
    }

    // Undead
    des.monster({ id: 'mummy' });
    des.monster({ id: 'mummy' });
    des.monster({ id: 'mummy' });
    des.monster({ id: 'mummy' });
    des.monster({ id: 'giant mummy' });
    des.monster({ id: 'giant mummy' });
    des.monster({ id: 'wraith' });
    des.monster({ id: 'wraith' });
    des.monster({ id: 'wraith' });
    des.monster({ id: 'spectre' });
    des.monster({ id: 'ghost' });
    des.monster({ id: 'ghost' });
    des.monster({ id: 'shade' });
    des.monster({ id: 'vampire' });

    // Random monsters
    for (let i = 0; i < 14; i++) {
        des.monster();
    }

    return finalize_level();
}
