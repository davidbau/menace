/**
 * Abyss Level (void between planes)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel');

    // Floating platforms in void
    des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|\
|...-------......-------.......-------......-------.......-------........|\
|...|.....|.....|.......|.....|.....|.....|.......|.....|.......|........|\
|...|.....|.....|.......|.....|.....|.....|.......|.....|.......|........|\
|...-------......-------........-------......-------.......-------........|\
|.........................................................................|\
|.........................................................................|\
|...-------......-------.......-------......-------.......-------........|\
|...|.....|.....|.......|.....|.....|.....|.......|.....|.......|........|\
|...|.....|.....|.......|.....|.....|.....|.......|.....|.......|........|\
|...-------......-------.......-------......-------.......-------........|\
|.........................................................................|\
|.........................................................................|\
|...-------......-------.......-------......-------.......-------........|\
|...|.....|.....|.......|.....|.....|.....|.......|.....|.......|........|\
|...|.....|.....|.......|.....|.....|.....|.......|.....|.......|........|\
|...-------......-------........-------......-------.......-------........|\
|.........................................................................|\
---------------------------------------------------------------------------
`
    });

    // Dark void
    des.region(selection.area(1, 1, 73, 17), 'unlit');

    // Stairs
    des.stair('up', 7, 4);
    des.stair('down', 63, 15);

    // Objects
    for (let i = 0; i < 15; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 15; i++) {
        des.trap();
    }

    // Void creatures
    des.monster({ id: 'energy vortex' });
    des.monster({ id: 'energy vortex' });
    des.monster({ id: 'black light' });
    des.monster({ id: 'black light' });
    des.monster({ id: 'yellow light' });
    des.monster({ id: 'shade' });
    des.monster({ id: 'shade' });
    des.monster({ id: 'wraith' });
    des.monster({ id: 'wraith' });
    des.monster({ id: 'ghost' });
    des.monster({ id: 'ghost' });

    // Random monsters
    for (let i = 0; i < 17; i++) {
        des.monster();
    }

    return finalize_level();
}
