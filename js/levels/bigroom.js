/**
 * Big Room (random variant for main dungeon)
 * Simplified port from nethack-c/dat/bigrm-1.lua
 *
 * This is one of several possible "big room" variants that can appear
 * as a random special room in the main dungeon.
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export async function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel', 'noflip');

    // One giant room filling most of the level
    await des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|........................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
---------------------------------------------------------------------------
`
    });

    // Light the whole room
    await des.region(selection.area(1, 1, 73, 16), 'lit');

    // Stairs
    await des.stair('up');
    await des.stair('down');

    // Non-diggable walls
    await des.non_diggable();

    // Lots of random objects (15)
    for (let i = 0; i < 15; i++) {
        await des.object();
    }

    // Random traps (6)
    for (let i = 0; i < 6; i++) {
        await des.trap();
    }

    // Lots of random monsters (28)
    for (let i = 0; i < 28; i++) {
        await des.monster();
    }

    return await finalize_level();
}
