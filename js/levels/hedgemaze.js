/**
 * Hedge Maze Level (garden labyrinth)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: '.' });

    des.level_flags('mazelevel');

    // Intricate hedge maze (using # for hedges)
    des.map({
        map: `
---------------------------------------------------------------------------
|#########|..............|###########|..............|#########|.........|\
|#########|..............|###########|..............|#########|.........|\
|.......|#####|......|##################|......|##################|.....|\
|.......|#####|......|##################|......|##################|.....|\
|...|#####|......|#####|......|#####|......|#####|......|#####|.........|\
|...|#####|......|#####|......|#####|......|#####|......|#####|.........|\
|.......|#####|......|#####|......|#####|......|#####|......|###########|\
|.......|#####|......|#####|......|#####|......|#####|......|###########|\
|...|##################|......|#####|......|##################|.........|\
|...|##################|......|#####|......|##################|.........|\
|.......|#####|......|#####|......|#####|......|#####|......|###########|\
|.......|#####|......|#####|......|#####|......|#####|......|###########|\
|...|#####|......|#####|......|#####|......|#####|......|#####|.........|\
|...|#####|......|#####|......|#####|......|#####|......|#####|.........|\
|.......|#####|......|##################|......|##################|.....|\
|.......|#####|......|##################|......|##################|.....|\
|#########|..............|###########|..............|#########|.........|\
|#########|..............|###########|..............|#########|.........|\
---------------------------------------------------------------------------
`
    });

    // Well-lit outdoor maze
    des.region(selection.area(0, 0, 75, 19), 'lit');

    // Stairs at opposite corners
    des.stair('up', 5, 5);
    des.stair('down', 68, 16);

    // Objects scattered throughout
    for (let i = 0; i < 15; i++) {
        des.object();
    }

    // Traps in the paths
    for (let i = 0; i < 12; i++) {
        des.trap();
    }

    // Maze guardians
    des.monster({ id: 'Minotaur' });
    des.monster({ id: 'wood nymph' });
    des.monster({ id: 'wood nymph' });
    des.monster({ id: 'guardian naga' });
    des.monster({ id: 'guardian naga' });
    des.monster({ id: 'soldier' });
    des.monster({ id: 'soldier' });
    des.monster({ id: 'soldier' });
    des.monster({ id: 'sergeant' });
    des.monster({ id: 'lieutenant' });
    des.monster({ id: 'tiger' });
    des.monster({ id: 'panther' });
    des.monster({ id: 'jaguar' });

    // Random monsters
    for (let i = 0; i < 15; i++) {
        des.monster();
    }

    return finalize_level();
}
