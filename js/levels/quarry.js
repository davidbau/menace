/**
 * Quarry Level (stone excavation)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: '.' });

    des.level_flags('mazelevel');

    // Open pit quarry
    des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|\
|.........................................................................|\
|.........................................................................|\
|.........................................................................|\
|......-----------------------------------------------------------.........|\
|....--|.........................................................|--........|\
|....--|.........................................................|.--......|\
|......|.........................................................|..--......|\
|......|.........................................................|...|.....|\
|......|.........................................................|...|.....|\
|......|.........................................................|..--......|\
|....--|.........................................................|.--......|\
|....--|.........................................................|--........|\
|......-----------------------------------------------------------.........|\
|.........................................................................|\
|.........................................................................|\
|.........................................................................|\
---------------------------------------------------------------------------
`
    });

    // Lit quarry
    des.region(selection.area(1, 1, 73, 17), 'lit');

    // Stairs
    des.stair('up', 10, 3);
    des.stair('down', 40, 10);

    // Gems and stones
    for (let i = 0; i < 25; i++) {
        des.object({ class: '*' });
    }
    for (let i = 0; i < 10; i++) {
        des.object();
    }

    // Mining hazards
    for (let i = 0; i < 12; i++) {
        des.trap();
    }

    // Quarry workers and creatures
    des.monster({ id: 'dwarf' });
    des.monster({ id: 'dwarf' });
    des.monster({ id: 'dwarf' });
    des.monster({ id: 'dwarf lord' });
    des.monster({ id: 'gnome' });
    des.monster({ id: 'gnome' });
    des.monster({ id: 'gnome lord' });
    des.monster({ id: 'rock mole' });
    des.monster({ id: 'rock mole' });
    des.monster({ id: 'rock piercer' });
    des.monster({ id: 'stone golem' });
    des.monster({ id: 'clay golem' });
    des.monster({ id: 'earth elemental' });
    des.monster({ id: 'xorn' });

    // Random monsters
    for (let i = 0; i < 14; i++) {
        des.monster();
    }

    return finalize_level();
}
