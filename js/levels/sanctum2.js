/**
 * Sanctum Variant Level (holy sanctuary)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel');

    // Sacred sanctuary
    des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|\
|.........................................................................|\
|........................---------------------------------................|\
|......................--|...............................|--..............|\
|.....................-|..................................|.--.............|\
|....................-|....................................|..|-...........|\
|...................-|......................................|...|..........|\
|...................|........................................|...|..........|\
|...................|........................................|...|..........|\
|...................|.......TTTTTTTTTTTTTTTTTTTTTTTTTTT.....|...|..........|\
|...................|........................................|...|..........|\
|...................|........................................|...|..........|\
|...................-|......................................|...|..........|\
|....................-|....................................|..|-...........|\
|.....................-|..................................|.--.............|\
|......................--|...............................|--..............|\
|........................---------------------------------................|\
|.........................................................................|\
---------------------------------------------------------------------------
`
    });

    // Radiantly lit
    des.region(selection.area(1, 1, 73, 17), 'lit');

    // Stairs
    des.stair('up', 5, 9);
    des.stair('down', 40, 9);

    // Non-diggable (holy ground)
    des.non_diggable();

    // Altar
    des.feature({ type: 'altar', x: 40, y: 10, alignment: 'law' });

    // Sacred treasures
    for (let i = 0; i < 15; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 8; i++) {
        des.trap();
    }

    // Divine guardians
    des.monster({ id: 'Angel' });
    des.monster({ id: 'Angel' });
    des.monster({ id: 'Angel' });
    des.monster({ id: 'Archon' });
    des.monster({ id: 'Archon' });
    des.monster({ id: 'ki-rin' });
    des.monster({ id: 'ki-rin' });
    des.monster({ id: 'couatl' });

    // Clergy
    des.monster({ id: 'high priest' });
    des.monster({ id: 'priest' });
    des.monster({ id: 'priest' });
    des.monster({ id: 'priestess' });
    des.monster({ id: 'priestess' });

    // Random monsters
    for (let i = 0; i < 15; i++) {
        des.monster();
    }

    return finalize_level();
}
