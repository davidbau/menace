/**
 * Oasis Level (desert spring)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: '.' });

    des.level_flags('mazelevel');

    // Oasis in desert
    des.map({
        map: `
.............................................................................
.............................................................................
.............................................................................
..................................}}}}}}}...................................
.................................}}}}}}}}}..................................
................................}}}}}}}}}}}.................................
...............................}}}}}}}}}}}}}................................
..............................}}}}}}}}}}}}}}................................
.............................}}}}}}}}}}}}}}}................................
.............................}}}}}}}}}}}}}}}................................
..............................}}}}}}}}}}}}}}................................
...............................}}}}}}}}}}}}}................................
................................}}}}}}}}}}}.................................
.................................}}}}}}}}}..................................
..................................}}}}}}}...................................
.............................................................................
.............................................................................
.............................................................................
.............................................................................
`
    });

    // Lit oasis
    des.region(selection.area(0, 0, 76, 19), 'lit');

    // Stairs
    des.stair('up', 10, 10);
    des.stair('down', 65, 10);

    // Objects
    for (let i = 0; i < 12; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 8; i++) {
        des.trap();
    }

    // Desert creatures
    des.monster({ id: 'crocodile' });
    des.monster({ id: 'crocodile' });
    des.monster({ id: 'giant eel' });
    des.monster({ id: 'cobra' });
    des.monster({ id: 'python' });
    des.monster({ id: 'rattlesnake' });
    des.monster({ id: 'scorpion' });
    des.monster({ id: 'giant scorpion' });
    des.monster({ id: 'fire ant' });
    des.monster({ id: 'soldier ant' });
    des.monster({ id: 'water nymph' });
    des.monster({ id: 'sand vortex' });

    // Random monsters
    for (let i = 0; i < 16; i++) {
        des.monster();
    }

    return finalize_level();
}
