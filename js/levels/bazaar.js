/**
 * Bazaar Level (marketplace)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel');

    // Market bazaar with stalls
    des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|\
|...-------.-------.-------.-------.-------.-------.-------.-------......|\
|...|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|......|\
|...|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|......|\
|...---+---.---+---.---+---.---+---.---+---.---+---.---+---.---+---......|\
|.........................................................................|\
|.........................................................................|\
|...-------.-------.-------.-------.-------.-------.-------.-------......|\
|...|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|......|\
|...|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|......|\
|...---+---.---+---.---+---.---+---.---+---.---+---.---+---.---+---......|\
|.........................................................................|\
|.........................................................................|\
|...-------.-------.-------.-------.-------.-------.-------.-------......|\
|...|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|......|\
|...|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|.|.....|......|\
|...---+---.---+---.---+---.---+---.---+---.---+---.---+---.---+---......|\
|.........................................................................|\
---------------------------------------------------------------------------
`
    });

    // Well-lit market
    des.region(selection.area(1, 1, 73, 17), 'lit');

    // Stairs
    des.stair('up', 5, 6);
    des.stair('down', 68, 12);

    // Stall doors
    for (let y of [5, 11, 17]) {
        for (let x of [9, 17, 25, 33, 41, 49, 57, 65]) {
            des.door('closed', x, y);
        }
    }

    // Non-diggable
    des.non_diggable();

    // Lots of merchandise
    for (let i = 0; i < 20; i++) {
        des.object();
    }
    for (let i = 0; i < 10; i++) {
        des.object({ class: '*' }); // Gems
    }
    for (let i = 0; i < 10; i++) {
        des.object({ class: '$' }); // Gold
    }

    // Traps
    for (let i = 0; i < 8; i++) {
        des.trap();
    }

    // Merchants and guards
    des.monster({ id: 'shopkeeper' });
    des.monster({ id: 'shopkeeper' });
    des.monster({ id: 'shopkeeper' });
    des.monster({ id: 'shopkeeper' });
    des.monster({ id: 'guard' });
    des.monster({ id: 'guard' });
    des.monster({ id: 'guard' });
    des.monster({ id: 'guard' });
    des.monster({ id: 'watchman' });
    des.monster({ id: 'watchman' });
    des.monster({ id: 'watch captain' });

    // Random monsters (customers)
    for (let i = 0; i < 17; i++) {
        des.monster();
    }

    return finalize_level();
}
