/**
 * Armory Level (weapons cache)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel');

    // Armory with weapon racks
    des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|\
|..---------.---------.---------.---------.---------.---------.----------|\
|..|.......|.|.......|.|.......|.|.......|.|.......|.|.......|.|.........|\
|..|.......|.|.......|.|.......|.|.......|.|.......|.|.......|.|.........|\
|..|.......|.|.......|.|.......|.|.......|.|.......|.|.......|.|.........|\
|..|.......|.|.......|.|.......|.|.......|.|.......|.|.......|.|.........|\
|..----+----.----+----.----+----.----+----.----+----.----+----.----+-----|\
|.........................................................................|\
|.........................................................................|\
|..---------.---------.---------.---------.---------.---------.----------|\
|..|.......|.|.......|.|.......|.|.......|.|.......|.|.......|.|.........|\
|..|.......|.|.......|.|.......|.|.......|.|.......|.|.......|.|.........|\
|..|.......|.|.......|.|.......|.|.......|.|.......|.|.......|.|.........|\
|..|.......|.|.......|.|.......|.|.......|.|.......|.|.......|.|.........|\
|..----+----.----+----.----+----.----+----.----+----.----+----.----+-----|\
|.........................................................................|\
---------------------------------------------------------------------------
`
    });

    // Well-lit armory
    des.region(selection.area(1, 1, 73, 15), 'lit');

    // Stairs
    des.stair('up', 5, 8);
    des.stair('down', 68, 8);

    // Doors
    for (let y of [7, 15]) {
        for (let x of [10, 20, 30, 40, 50, 60, 69]) {
            des.door('closed', x, y);
        }
    }

    // Non-diggable (armory vault)
    des.non_diggable();

    // Massive weapons cache
    for (let i = 0; i < 30; i++) {
        des.object({ class: ')' }); // Weapons
    }
    for (let i = 0; i < 30; i++) {
        des.object({ class: '[' }); // Armor
    }
    for (let i = 0; i < 15; i++) {
        des.object({ class: '(' }); // Tools
    }
    for (let i = 0; i < 10; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 10; i++) {
        des.trap();
    }

    // Guards
    des.monster({ id: 'captain' });
    des.monster({ id: 'captain' });
    des.monster({ id: 'lieutenant' });
    des.monster({ id: 'lieutenant' });
    des.monster({ id: 'sergeant' });
    des.monster({ id: 'sergeant' });
    for (let i = 0; i < 10; i++) {
        des.monster({ id: 'soldier' });
    }
    des.monster({ id: 'guard' });
    des.monster({ id: 'guard' });
    des.monster({ id: 'guard' });

    // Random monsters
    for (let i = 0; i < 5; i++) {
        des.monster();
    }

    return finalize_level();
}
